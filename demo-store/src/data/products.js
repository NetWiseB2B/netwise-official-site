export function getProductByHandle(handle) {
  return products.find(p => p.handle === handle);
}

export function getProductsByCollection(collectionHandle) {
  if (collectionHandle === 'all') return products;
  const collection = collections.find(c => c.handle === collectionHandle);
  if (!collection) return [];
  return products.filter(p => p.collection === collection.id);
}

export function getB2BPrice(product, quantity = 1) {
  if (!product.quantityBreaks) return product.b2bPrice;
  const tier = product.quantityBreaks.find(
    t => quantity >= t.min && (t.max === null || quantity <= t.max)
  );
  return tier ? tier.price : product.b2bPrice;
}

export function getVariantB2BPrice(variant, product, totalQty = 1) {
  if (!product.quantityBreaks) return variant.price;
  const tier = product.quantityBreaks.find(
    t => totalQty >= t.min && (t.max === null || totalQty <= t.max)
  );
  return tier ? Math.min(tier.price, variant.price) : variant.price;
}
